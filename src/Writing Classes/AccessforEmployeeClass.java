public class Employee
{
    private String firstName;
    private String lastName;
    private double salary;
    private int shiftHours;
    
    public Employee (String fName, String lName, double theSalary, int shiftHrs) {
        firstName = fName;
        lastName = lName;
        salary = theSalary;
        shiftHours = shiftHrs;
    }
}